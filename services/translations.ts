type Translations = typeof translations.en;
export type TKey = keyof Translations;

export const createTranslator = (language: Language) => {
    return (key: TKey, replacements?: Record<string, string | number>): string => {
        const langDict = translations[language] || translations.en;
        let text = langDict[key] || translations.en[key] || String(key);

        if (replacements) {
            Object.keys(replacements).forEach(rKey => {
                text = text.replace(new RegExp(`{{${rKey}}}`, 'g'), String(replacements[rKey]));
            });
        }
        return text;
    };
};